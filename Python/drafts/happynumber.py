    my_list=[]
    while True:
        str_num = str(n)
        sums = 0
        for i in str_num:
            sums += (int(i)**2)
        print(sums) # can be omit after all done
        if sums==1:
            return 1
        elif sums in my_list:
            return 0
        my_list.append(sums)
        print(my_list) # can be omit after all done
        n = sums        
                
